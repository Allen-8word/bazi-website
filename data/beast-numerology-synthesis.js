  function dedupePush(arr, item){
    if(arr.indexOf(item) === -1) arr.push(item);
  }

  /** 適合發展領域：日主前 2 + 靈數前 3，去重回補至 5 */
  function getFields(dayStem, lifePath){
    var dm = DAY_MASTER_FIELDS[dayStem];
    var nm = NUMBER_FIELDS[lifePath];
    if(!dm || !nm) return null;
    var out = [];
    dedupePush(out, dm[0]);
    dedupePush(out, dm[1]);
    for(var i = 0; i < nm.length && out.length < 5; i++) dedupePush(out, nm[i]);
    if(out.length < 5 && dm[2]) dedupePush(out, dm[2]);
    return out.slice(0, 5);
  }

  /** 幸運元素：日主 3 + 靈數 2 */
  function getLucky(dayStem, lifePath){
    var dm = DAY_MASTER_LUCKY[dayStem];
    var nm = NUMBER_LUCKY[lifePath];
    if(!dm || !nm) return null;
    return dm.concat(nm.slice(0, 2));
  }

  /** 完整合成資料 */
